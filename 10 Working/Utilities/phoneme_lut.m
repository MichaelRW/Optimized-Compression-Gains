function [type] = phoneme_lut(phone)

% if ~ischar(phone)
%         disp('Function requires a character string');
%         type = '';
%     return
% end

Stops = sum(strcmp(phone, {'b',...
'd',...
'g',...
'p',...
't',...
'k',...
'dx',...
'q'}));

Affricates = sum( strcmp(phone, {'jh', 'ch'}));

Fricatives = sum( strcmp(phone, {'s',...
'sh',...
'z',...
'zh',...
'f',...
'th',...
'v',...
'dh'}));

Nasals = sum( strcmp(phone, {'m',...
'n',...
'ng',...
'em',...
'en',...
'eng',...
'nx'}));
              
Glides = sum( strcmp(phone, {'l',...
'r',...
'w',...
'y',...
'hh',...
'hv',...
'el'}));


Vowels = sum( strcmp(phone, {'iy',...
'ih',...
'eh',...
'ey',...
'ae',...
'aa',...
'aw',...
'ay',...
'ah',...
'ao',...
'oy',...
'ow',...
'uh',...
'uw',...
'ux',...
'er',...
'ax',...
'ix',...
'axr',...
'ax-h'}));


Others = sum( strcmp(phone, {'pau',...
'epi',...
'h#',...
'1',...
'2'}));

Closures = sum( strcmp(phone, {'bcl',...
'dcl',...
'gcl',...
'pcl',...
'tck',...
'kcl',...
'tcl'}));

if Stops
    type = 'Stops';
elseif Affricates
    type = 'Affricates';
elseif Fricatives
    type = 'Fricatives';
elseif Nasals
    type = 'Nasals';
elseif Glides
    type = 'Glides';
elseif Vowels
    type = 'Vowels';
elseif Others
    type = 'Others';
elseif Closures
    type = 'Closures';
else
    type = '';
    phone
    disp('Phone not found!');
end